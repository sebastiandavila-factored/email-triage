from __future__ import annotations

from pydantic_ai import Agent
from pydantic_ai.models.groq import GroqModel
from pydantic_ai.providers.groq import GroqProvider
from pydantic_ai.settings import ModelSettings

from evals.schemas import EvalCase, JudgeScore

_JUDGE_SYSTEM_PROMPT = """\
You are an expert evaluator of customer support email replies for an e-commerce store.

You will receive the original customer email (subject + body) and a draft reply
generated by an AI assistant.

Score the draft reply on these five dimensions:

- relevance (1–5): Does the reply directly address what the customer asked?
  1 = completely off-topic, 5 = perfectly on-point with no irrelevant content.

- language_match (true/false): Is the reply written in the same language as the customer's email?
  true = same language, false = different language.

- tone (1–5): Is the reply professional, empathetic, and courteous?
  1 = rude, robotic, or dismissive. 5 = warm, professional, and appropriately empathetic.

- correctness (1–5): Does the reply avoid making false, invented, or unverifiable claims?
  1 = contains clearly wrong information. 5 = factually safe, no invented details.

- overall (1–5): Your holistic quality score.
  1 = very poor (customer would be frustrated). 5 = excellent (customer would be satisfied).

Be strict: reserve 5 for replies with no room for improvement.
Do not penalize for generic replies that cannot know specific order details — that is expected.\
"""


class JudgeAgent:
    def __init__(self, api_key: str, model: str = "llama-3.3-70b-versatile") -> None:
        groq_model = GroqModel(model, provider=GroqProvider(api_key=api_key))
        self._agent: Agent[None, JudgeScore] = Agent(
            groq_model,
            output_type=JudgeScore,
            system_prompt=_JUDGE_SYSTEM_PROMPT,
            model_settings=ModelSettings(temperature=0.1),
        )

    async def evaluate(self, case: EvalCase, draft_reply: str) -> JudgeScore:
        user_msg = (
            f"Customer email:\n"
            f"Subject: {case.subject}\n"
            f"Body: {case.body}\n\n"
            f"Draft reply:\n{draft_reply}"
        )
        result = await self._agent.run(user_msg)
        return result.output
